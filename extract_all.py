import re

with open('app.jsx', 'r', encoding='utf-8') as f:
    content = f.read()

# Find everything between const defaultProducts = [ and ];
start_idx = content.find('const defaultProducts = [')
end_idx = content.find('];', start_idx)
products_block = content[start_idx:end_idx]

# Let's find each individual object block { ... } inside defaultProducts
object_blocks = re.findall(r'\{[^{}]*?\}', products_block, re.DOTALL)

print(f"Found {len(object_blocks)} blocks.")

sql_inserts = []
for block in object_blocks:
    # Extract keys
    id_match = re.search(r'id:\s*(\d+)', block)
    name_match = re.search(r'name:\s*"(.*?)"', block)
    price_match = re.search(r'price:\s*(\d+)', block)
    orig_price_match = re.search(r'original_price:\s*(\d+)', block)
    category_match = re.search(r'category:\s*"(.*?)"', block)
    image_match = re.search(r'image:\s*"(.*?)"', block)
    desc_match = re.search(r'desc:\s*"(.*?)"', block)
    stock_match = re.search(r'stock:\s*(\d+)', block)
    sold_match = re.search(r'sold:\s*(\d+)', block)
    
    if id_match and name_match and price_match and category_match:
        p_id = id_match.group(1)
        name = name_match.group(1).replace("'", "''")
        price = price_match.group(1)
        orig_price = orig_price_match.group(1) if orig_price_match else "NULL"
        category = category_match.group(1).replace("'", "''")
        image = image_match.group(1).replace("'", "''") if image_match else ""
        desc = desc_match.group(1).replace("'", "''") if desc_match else ""
        stock = stock_match.group(1) if stock_match else "0"
        sold = sold_match.group(1) if sold_match else "0"
        
        sql = f"({p_id}, '{name}', {price}, {orig_price}, '{category}', '{image}', '{desc}', {stock}, {sold})"
        sql_inserts.append(sql)

joined_sql = ",\n".join(sql_inserts)

